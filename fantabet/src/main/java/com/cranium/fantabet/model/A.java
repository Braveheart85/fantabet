package com.cranium.fantabet.model;

public class A {
	private String a;
	private String b;

	public A(String a, String b) {

		this.setA(a);
		this.setB(b);
	}

	public String getA() {
		return a;
	}

	public void setA(String a) {
		this.a = a;
	}

	public String getB() {
		return b;
	}

	public void setB(String b) {
		this.b = b;
	}
}
